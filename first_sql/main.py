# 7. Средняя сумма заказа по каждой должности сотрудника
cursor.execute("""
    SELECT д.`Название` AS должность,
           COUNT(з.`Код_заказа`) AS кол_заказов,
           ROUND(AVG(з.`Сумма`), 2) AS средняя_сумма
    FROM `Заказы` з
    JOIN `Сотрудники` с ON з.`Код_сотрудника` = с.`Код_сотрудника`
    JOIN `Должности`  д ON с.`Код_должности`  = д.`Код_должности`
    GROUP BY д.`Код_должности`
    ORDER BY средняя_сумма DESC
""")

