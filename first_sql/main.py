# 11. ТОП-3 сотрудника по суммарной выручке (только выполненные заказы)
cursor.execute("""
    SELECT с.`Фамилия` || ' ' || с.`Имя` AS сотрудник,
           д.`Название`                   AS должность,
           COUNT(з.`Код_заказа`)           AS заказов,
           SUM(з.`Сумма`)                  AS выручка
    FROM `Заказы` з
    JOIN `Сотрудники` с ON з.`Код_сотрудника` = с.`Код_сотрудника`
    JOIN `Должности`  д ON с.`Код_должности`  = д.`Код_должности`
    WHERE з.`Отметка_о_выполнении` = 1
    GROUP BY с.`Код_сотрудника`
    ORDER BY выручка DESC
    LIMIT 3
""")

