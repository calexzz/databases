# 6. Количество заказов и суммарная выручка по каждому клиенту
cursor.execute("""
    SELECT к.`Организация`,
           COUNT(з.`Код_заказа`)  AS кол_заказов,
           SUM(з.`Сумма`) AS итого
    FROM `Заказы` з
    JOIN `Клиенты` к ON з.`Код_клиента` = к.`Код_клиента`
    GROUP BY к.`Код_клиента`
    ORDER BY итого DESC
""")

print("\n6. Заказы и выручка по клиентам:")
